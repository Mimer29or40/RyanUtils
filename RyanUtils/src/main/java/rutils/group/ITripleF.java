package rutils.group;

/**
 * A simple {@code double} float.
 */
public interface ITripleF extends ITriple<Float, Float, Float>
{
    /**
     * @return The first float value.
     */
    float a();
    
    /**
     * @return The second float value.
     */
    float b();
    
    /**
     * @return The third float value.
     */
    float c();
}
